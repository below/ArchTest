import UIKit

class SVKEmptyTableViewState: UIView {
    @IBOutlet weak var contentView: UIView!
    @IBOutlet weak var imageView: SVKStandardImageView!
    @IBOutlet weak var titleLbl: SVKStandardPageMessageTitle!
    @IBOutlet weak var descriptionLbl: SVKStandardPageMessageDescription!
    
    /*
    // Only override draw() if you perform custom drawing.
    // An empty implementation adversely affects performance during animation.
    override func draw(_ rect: CGRect) {
        // Drawing code
    }
    */

    override init(frame: CGRect) {
        super.init(frame: frame)
        setupView()
    }
    
    required init?(coder aDecoder: NSCoder) {
        super.init(coder: aDecoder)
        setupView()
    }
    
    func setupView() {
        SVKBundle.loadNibNamed("SVKEmptyTableViewState", owner: self, options: nil)
        contentView.frame = self.frame
        addSubview(contentView)
    }
}
